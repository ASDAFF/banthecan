        <h1>Wilkommen zum Ban-The-Can</h1>

        <p><strong>Ban-The-Can</strong> is a Webproject that can be used for
            management of agenda topics (tickets). It contains a <strong>Backlog</strong>
            for collection topics, a <strong>Board</strong> for managing current
            working topics, and a <strong>Completed log</strong> for tracking topics
            which have been finished.</p>

        <p>A central feature is the <strong>Board</strong> which allows you to
            define stages (columns) of a workflow for visualizing and managing the
            steps needed to bring a topic to completion. An example scenario would
            be (<em>Discussion, Creating Tasks, Writing a Protocol</em>). This would
            be something that applies to an organization that needs a tool for the
            management of discussion topics and the steps that accompany them.
            However, because the workflow is fully definable it can be used for a
            variety of workflow processes. Included in the workflow definition are
            rules for restricting the movement of topics between the defined phases,
            thus forcing that the workflow is followed.</p>

        <p>The name is a deliberate play on words of the <a
                href="https://en.wikipedia.org/wiki/Kanban">Kanban scheduling system</a>
            and has been inspired by software which uses the Kanban system. Please
            explore it's features and discover if it is useful for your needs. It is
            an opensource project and therefore free to use. For detailed
            information please refer to the <a href="/site/about">About page.</a></p>

